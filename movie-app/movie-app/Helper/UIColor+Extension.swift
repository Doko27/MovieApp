import Foundation
import UIKit

// Color palette
extension UIColor {
    
    @nonobjc public static var riseOrange: UIColor {
        return UIColor(red: 255.0 / 255.0, green: 150.0 / 255.0, blue: 53.0 / 255.0, alpha: 1.0)
    }
    
    @nonobjc public static var graySoft: UIColor {
        return UIColor(red: 235.0 / 255.0, green: 235.0 / 255.0, blue: 235.0 / 255.0, alpha: 1.0)
    }

}
